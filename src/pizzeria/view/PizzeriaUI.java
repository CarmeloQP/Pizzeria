    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed

         // 1. Mapear el combo de tamaño al enum Size
    Size size;
    switch (cbSize.getSelectedIndex()) {
        case 0 -> size = Size.PERSONAL;
        case 1 -> size = Size.MEDIANA;
        case 2 -> size = Size.FAMILIAR;
        default -> size = Size.PERSONAL; // por si acaso
    }

    // 2. Mapear el combo de masa al enum Masa
    Masa masa;
    switch (cbMasa.getSelectedIndex()) { // OJO: cbMasa, no cbSalsa
        case 0 -> masa = Masa.DELGADA;
        case 1 -> masa = Masa.TRADICIONAL;
        case 2 -> masa = Masa.PAN;
        default -> masa = Masa.TRADICIONAL;
    }

    // 3. Mapear el combo de salsa al enum Salsa
    Salsa salsa;
    switch (cbSalsa.getSelectedIndex()) {
        case 0 -> salsa = Salsa.TOMATE;
        case 1 -> salsa = Salsa.BLANCA;
        case 2 -> salsa = Salsa.BBQ;
        case 3 -> salsa = Salsa.PESTO;
        default -> salsa = Salsa.TOMATE;
    }

    // 4. Construir la pizza con el Builder
    Pizza.Builder builder = new Pizza.Builder()
            .size(size)
            .masa(masa)
            .salsa(salsa);

    if (chkBordes.isSelected()) {
        builder.bordesRellenos(true);
    }
    if (chkSinGluten.isSelected()) {
        builder.sinGluten(true);
    }

    Pizza pizza = builder.build();
    txtResumen.setText(pizza.toString());

        
        

    }//GEN-LAST:event_btnCrearActionPerformed

